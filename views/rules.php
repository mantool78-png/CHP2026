    <h2>7. Победители и призовой фонд</h2>
    <p>Победителями конкурса становятся 10 участников, набравших наибольшее количество очков по итогам всего турнира.</p>
    <p>Призовой фонд выплачивается реальными деньгами.</p>
    <p>Пропорции распределения призового фонда между 10 победителями будут опубликованы отдельно до начала турнира.</p>

