<section class="card rules-page">
    <h2>1. Какие данные собираются</h2>
    <p>
        При регистрации сайт сохраняет имя участника, email, пароль в зашифрованном виде, дату регистрации,
        подтверждение возраста/согласия с условиями, статус оплаты, прогнозы, очки и действия, необходимые для работы конкурса.
    </p>
    <p>
        Организаторы не просят паспортные данные на этапе регистрации. Для решения спорных платежей или выплаты приза
        могут потребоваться дополнительные сведения, которые запрашиваются отдельно.
    </p>

    <h2>2. Для чего используются данные</h2>
    <ul>
        <li>создание и защита аккаунта участника;</li>
        <li>приём прогнозов и расчёт очков;</li>
        <li>подтверждение оплаты и формирование призового фонда;</li>
        <li>публикация имени участника в таблицах рейтинга, мини-лигах и прогнозах после начала матча;</li>
        <li>связь по вопросам конкурса, оплаты и спорных ситуаций.</li>
    </ul>

    <h2>3. Публичность прогнозов</h2>
    <p>
        После начала матча прогнозы участников по этому матчу становятся открытыми для просмотра на сайте.
        Имя участника и его место в таблице также могут отображаться публично.
    </p>

    <h2>4. Хранение и безопасность</h2>
    <p>
        Пароли не хранятся в открытом виде. Доступ к административным функциям ограничен аккаунтом администратора.
        Участник отвечает за сохранность своего пароля.
    </p>
    <p>
        Данные хранятся на сервере хостинга, используемого для работы сайта. Организаторы используют данные только для проведения конкурса
        и связанных организационных действий.
    </p>

    <h2>5. Обращения участника</h2>
    <p>
        Участник может обратиться к организаторам по вопросам доступа к аккаунту, исправления данных, статуса оплаты или удаления аккаунта.
        Контакты указаны внизу сайта и в личном кабинете.
    </p>

    <h2>6. Согласие</h2>
    <p>
        Создавая аккаунт, участник подтверждает, что ознакомился с правилами конкурса, условиями участия
        и настоящим уведомлением об обработке персональных данных.
    </p>
