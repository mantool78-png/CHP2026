    <h2>3. Прогнозы на матчи</h2>
    <p>Участник может оставлять прогнозы на матчи Чемпионата мира 2026.</p>
    <p>Прогноз включает предполагаемый счет матча.</p>
    <p>
        Прием прогнозов на каждый матч заканчивается за
        <strong><?= (int) config('app.prediction_lock_minutes') ?> минут</strong> до начала встречи.
    </p>
    <p>После закрытия приема прогнозов изменить прогноз на этот матч нельзя.</p>
    <p>После начала матча прогнозы всех участников становятся открытыми для просмотра.</p>

    <h2>4. Начисление очков</h2>
    <p>Очки начисляются после завершения матча и внесения финального счета администратором.</p>
    <ul>
        <li><strong>1 очко</strong> — за угаданный исход матча.</li>
        <li><strong>3 очка</strong> — за угаданный точный счет.</li>
        <li><strong>10 очков</strong> — за угаданного чемпиона мира.</li>
    </ul>
    <p>
        Исходом матча считается победа первой команды, ничья или победа второй команды.
        Если участник угадал точный счет, он получает 3 очка за матч.
        Дополнительное очко за исход в этом случае не начисляется.
    </p>

    <h2>5. Прогноз на чемпиона</h2>
    <p>Участник может выбрать команду, которая, по его мнению, станет чемпионом мира.</p>
    <p>За правильно угаданного чемпиона мира участник получает 10 дополнительных очков.</p>
    <p>
        Выбрать или изменить прогноз на чемпиона можно до дедлайна:
